    private void datatabel(){
    DefaultTableModel tbl = new DefaultTableModel();
        tbl.addColumn("ID Penumpang");
        tbl.addColumn("Nama Kereta");
        tbl.addColumn("Jenis Kereta");
        tbl.addColumn("Jumlah Tiket");
        tbl.addColumn("Harga Tiket");
        tbl.addColumn("Biaya Asuransi");
        tbl.addColumn("Biaya");
        tbl.addColumn("Potongan");
        tbl.addColumn("PPN");
        tbl.addColumn("Total Bayar");
        tbl.addColumn("Jumlah Uang");
        tbl.addColumn("Kembalian");
        tabel_kereta.setModel(tbl);
        
        try{
            Statement input = (Statement) konek.getConnection().createStatement();
            ResultSet res = input.executeQuery("SELECT * FROM kereta_api");
            while(res.next())
            {
                tbl.addRow(new Object[] {
                res.getString("id"),
                res.getString("nama_kereta"),
                res.getString("jenis_kereta"),        
                res.getString("jumlah_tiket"),
                res.getString("harga_tiket"),
                res.getString("biaya_asuransi"),
                res.getString("biaya"),
                res.getString("potongan"),
                res.getString("ppn"),
                res.getString("total_bayar"),
                res.getString("jumlah_uang"),
                res.getString("kembalian")
                }
                );
                tabel_kereta.setModel(tbl);
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
}
    
