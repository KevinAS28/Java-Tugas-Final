    private void radiobutton(){
        if(radio_anak.isSelected()){
            radio_dewasa.setSelected(false);
            radio_lansia.setSelected(false);
            
            int potongan = Integer.parseInt(String.valueOf(text_biaya.getText()));
            int persen = (int)((0.15)*potongan);
            text_asuransi.setText(String.valueOf(persen));
        }else if(radio_dewasa.isSelected()){
           radio_anak.setSelected(false);
            radio_lansia.setSelected(false);
            
            int potongan = Integer.parseInt(String.valueOf(text_biaya.getText()));
            int persen = (int)((0.10)*potongan);
            text_asuransi.setText(String.valueOf(persen)); 
        }else if(radio_lansia.isSelected()){
           radio_anak.setSelected(false);
            radio_dewasa.setSelected(false);
            
            int potongan = Integer.parseInt(String.valueOf(text_biaya.getText()));
            int persen = (int)((0.05)*potongan);
            text_asuransi.setText(String.valueOf(persen)); 
        }
        int asuransi = Integer.parseInt(String.valueOf(text_asuransi.getText()));
        int biaya = Integer.parseInt(String.valueOf(text_biaya.getText()));
        int potong = biaya - asuransi;
        text_potongan.setText(String.valueOf(potong));
        
        int pajak = Integer.parseInt(String.valueOf(text_biaya.getText()));
        int ppn = (int) ((0.10)* pajak);
        text_ppn.setText(String.valueOf(ppn));
        
        int fasilitas = Integer.parseInt(String.valueOf(text_fasilitas.getText()));
        int potong2 = Integer.parseInt(String.valueOf(text_potongan.getText()));
        int ppn2 = Integer.parseInt(String.valueOf(text_ppn.getText()));
        int total =  potong2 + ppn2 + fasilitas;
        text_total.setText(String.valueOf(total));
        
    }
    
